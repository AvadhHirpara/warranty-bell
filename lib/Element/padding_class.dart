import 'package:WarrantyBell/Element/responsive_size_value.dart';
import 'package:flutter/cupertino.dart';

///Padding Top
Widget paddingTop(double top){
  return Padding(padding: EdgeInsets.only(top: setSp(top)));
}

///Padding Left
Widget paddingLeft(double left){
  return Padding(padding: EdgeInsets.only(top: setSp(left)));
}

///Padding Right
Widget paddingRight(double right){
  return Padding(padding: EdgeInsets.only(top: setSp(right)));
}

///Padding Bottom
Widget paddingBottom(double bottom){
  return Padding(padding: EdgeInsets.only(top: setSp(bottom)));
}